import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { ISede } from '@/shared/model/sede.model';

const baseApiUrl = 'api/sedes';

export default class SedeService {
  public find(id: number): Promise<ISede> {
    return new Promise<ISede>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public findByCodigoIndicativo(codigoIndicativo: string): Promise<ISede> {
    return new Promise<ISede>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/find-by-codigo-indicativo`, {
          params: {
            codigo_indicativo: codigoIndicativo,
          },
        })
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ISede): Promise<ISede> {
    return new Promise<ISede>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ISede): Promise<ISede> {
    return new Promise<ISede>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: ISede): Promise<ISede> {
    return new Promise<ISede>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
